/**
 * Multi-entry Vite build script.
 *
 * Builds each integration as a separate IIFE file so the Rust server can
 * concatenate only the enabled modules at runtime.
 *
 * Output (in ../dist/):
 *   tsjs-core.js          — core API (always included)
 *   tsjs-<integration>.js — one per discovered integration
 *
 * Environment variables:
 *   TSJS_PREBID_ADAPTERS — Comma-separated list of Prebid.js bid adapter
 *     names to include in the bundle (e.g. "rubicon,appnexus,openx").
 *     Each name must have a corresponding {name}BidAdapter.js module in
 *     the prebid.js package. Default: "rubicon".
 *   TSJS_PREBID_USER_IDS — Comma-separated list of Prebid.js User ID
 *     submodule filenames (no `.js` extension) to include in the bundle
 *     (e.g. "sharedIdSystem,id5IdSystem,criteoIdSystem"). The `userId.js`
 *     core module is always included and is not configurable here.
 *     Default: the full ship-set of 13 submodules (see
 *     DEFAULT_PREBID_USER_IDS below).
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from 'vite';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const srcDir = path.resolve(__dirname, 'src');
const distDir = path.resolve(__dirname, '..', 'dist');
const integrationsDir = path.join(srcDir, 'integrations');

// ---------------------------------------------------------------------------
// Prebid adapter generation
// ---------------------------------------------------------------------------

const DEFAULT_PREBID_ADAPTERS = 'rubicon';
const ADAPTERS_FILE = path.join(
  integrationsDir,
  'prebid',
  '_adapters.generated.ts',
);

/**
 * Generate `_adapters.generated.ts` with import statements for each adapter
 * listed in the TSJS_PREBID_ADAPTERS environment variable.
 *
 * Invalid adapter names (those without a matching module in prebid.js) are
 * logged and skipped.
 */
function generatePrebidAdapters() {
  const raw = process.env.TSJS_PREBID_ADAPTERS || DEFAULT_PREBID_ADAPTERS;
  const names = raw
    .split(',')
    .map((s) => s.trim())
    .filter(Boolean);

  if (names.length === 0) {
    console.warn(
      '[build-all] TSJS_PREBID_ADAPTERS is empty, falling back to default:',
      DEFAULT_PREBID_ADAPTERS,
    );
    names.push(DEFAULT_PREBID_ADAPTERS);
  }

  const modulesDir = path.join(
    __dirname,
    'node_modules',
    'prebid.js',
    'modules',
  );

  // Validate each adapter and build import lines
  const imports = [];
  for (const name of names) {
    const moduleFile = `${name}BidAdapter.js`;
    const modulePath = path.join(modulesDir, moduleFile);
    if (!fs.existsSync(modulePath)) {
      console.error(
        `[build-all] WARNING: Prebid adapter "${name}" not found (expected ${moduleFile}), skipping`,
      );
      continue;
    }
    imports.push(`import 'prebid.js/modules/${moduleFile}';`);
  }

  if (imports.length === 0) {
    console.error(
      '[build-all] WARNING: No valid Prebid adapters found, bundle will have no client-side adapters',
    );
  }

  const content = [
    '// Auto-generated by build-all.mjs — manual edits will be overwritten at build time.',
    '//',
    '// Controls which Prebid.js bid adapters are included in the bundle.',
    '// Set the TSJS_PREBID_ADAPTERS environment variable to a comma-separated list',
    '// of adapter names (e.g. "rubicon,appnexus,openx") before building.',
    `// Default: "${DEFAULT_PREBID_ADAPTERS}"`,
    '',
    ...imports,
    '',
  ].join('\n');

  fs.writeFileSync(ADAPTERS_FILE, content);

  const adapterNames = names.filter((name) =>
    fs.existsSync(path.join(modulesDir, `${name}BidAdapter.js`)),
  );
  console.log('[build-all] Prebid adapters:', adapterNames);
}

generatePrebidAdapters();

// ---------------------------------------------------------------------------
// Prebid User ID submodule generation
// ---------------------------------------------------------------------------

/**
 * Default set of Prebid User ID submodules bundled when TSJS_PREBID_USER_IDS
 * is unset. Matches the set originally hardcoded in index.ts when User ID
 * support first shipped. `userId.js` (the core module) is imported
 * unconditionally by index.ts and is not in this list.
 */
const DEFAULT_PREBID_USER_IDS = [
  'sharedIdSystem',
  'criteoIdSystem',
  '33acrossIdSystem',
  'pubProvidedIdSystem',
  'quantcastIdSystem',
  'id5IdSystem',
  'identityLinkIdSystem',
  'uid2IdSystem',
  'euidIdSystem',
  'intentIqIdSystem',
  'lotamePanoramaIdSystem',
  'connectIdSystem',
  'merkleIdSystem',
].join(',');

/**
 * Modules known to be incompatible with the current esbuild pipeline.
 *
 * `liveIntentIdSystem` uses a dynamic `require()` inside a build-flag-guarded
 * branch that Prebid's own gulp pipeline dead-codes via constant folding.
 * esbuild leaves the `require()` in the bundle, which throws
 * `ReferenceError: require is not defined` at browser runtime. See spec
 * follow-up #4 in `docs/superpowers/specs/2026-04-16-prebid-user-id-module-design.md`.
 */
const PREBID_USER_ID_DENYLIST = new Set(['liveIntentIdSystem']);

const USER_IDS_FILE = path.join(
  integrationsDir,
  'prebid',
  '_user_ids.generated.ts',
);

/**
 * Generate `_user_ids.generated.ts` with import statements for each User ID
 * submodule listed in the TSJS_PREBID_USER_IDS environment variable.
 *
 * Invalid submodule names (those without a matching module in prebid.js) or
 * known-broken modules in the denylist are logged and skipped.
 */
function generatePrebidUserIds() {
  const raw = process.env.TSJS_PREBID_USER_IDS || DEFAULT_PREBID_USER_IDS;
  const names = raw
    .split(',')
    .map((s) => s.trim())
    .filter(Boolean);

  if (names.length === 0) {
    console.warn(
      '[build-all] TSJS_PREBID_USER_IDS is empty, falling back to default set',
    );
    names.push(...DEFAULT_PREBID_USER_IDS.split(','));
  }

  const modulesDir = path.join(
    __dirname,
    'node_modules',
    'prebid.js',
    'modules',
  );

  const imports = [];
  const includedNames = [];
  for (const name of names) {
    if (PREBID_USER_ID_DENYLIST.has(name)) {
      console.error(
        `[build-all] WARNING: Prebid User ID submodule "${name}" is on the ` +
          `esbuild-incompatibility denylist and will not be bundled. See ` +
          `docs/superpowers/specs/2026-04-16-prebid-user-id-module-design.md ` +
          `follow-up #4.`,
      );
      continue;
    }
    const moduleFile = `${name}.js`;
    const modulePath = path.join(modulesDir, moduleFile);
    // Some modules ship as .ts in modules/ but resolve via the exports map
    // to dist/src/public/*.js (e.g. sharedIdSystem). Accept either form.
    const distPath = path.join(__dirname, 'node_modules', 'prebid.js', 'dist', 'src', 'public', moduleFile);
    if (!fs.existsSync(modulePath) && !fs.existsSync(distPath)) {
      console.error(
        `[build-all] WARNING: Prebid User ID submodule "${name}" not found (expected ${moduleFile}), skipping`,
      );
      continue;
    }
    imports.push(`import 'prebid.js/modules/${moduleFile}';`);
    includedNames.push(name);
  }

  if (imports.length === 0) {
    console.error(
      '[build-all] WARNING: No valid Prebid User ID submodules found, ' +
        'bundle will resolve no EIDs even if publisher configures userSync.userIds',
    );
  }

  const content = [
    '// Auto-generated by build-all.mjs — manual edits will be overwritten at build time.',
    '//',
    '// Controls which Prebid.js User ID submodules are included in the bundle.',
    '// Set the TSJS_PREBID_USER_IDS environment variable to a comma-separated',
    '// list of submodule filenames without the `.js` extension',
    '// (e.g. "sharedIdSystem,id5IdSystem,criteoIdSystem") before building.',
    '// The userId.js core module is always included via a static import in',
    '// index.ts and is not configurable here.',
    '',
    ...imports,
    '',
  ].join('\n');

  fs.writeFileSync(USER_IDS_FILE, content);

  console.log('[build-all] Prebid User ID submodules:', includedNames);
}

generatePrebidUserIds();

// ---------------------------------------------------------------------------

// Clean dist directory
fs.rmSync(distDir, { recursive: true, force: true });
fs.mkdirSync(distDir, { recursive: true });

// Discover integration modules: directories in src/integrations/ with index.ts
const integrationModules = fs.existsSync(integrationsDir)
  ? fs
      .readdirSync(integrationsDir)
      .filter((name) => {
        const fullPath = path.join(integrationsDir, name);
        return (
          fs.statSync(fullPath).isDirectory() &&
          fs.existsSync(path.join(fullPath, 'index.ts'))
        );
      })
      .sort()
  : [];

console.log('[build-all] Discovered integrations:', integrationModules);

/** Build a single module as a self-contained IIFE. */
async function buildModule(name, entryPath) {
  const outFile = `tsjs-${name}.js`;
  console.log(`[build-all] Building ${outFile} from ${path.relative(__dirname, entryPath)}`);

  await build({
    configFile: false,
    root: __dirname,
    resolve: {
      alias: {
        // prebid.js doesn't expose src/adapterManager.js via its package
        // "exports" map, but we need it for client-side bidder validation.
        'prebid.js/src/adapterManager.js': path.resolve(
          __dirname,
          'node_modules/prebid.js/dist/src/src/adapterManager.js',
        ),
      },
    },
    build: {
      emptyOutDir: false,
      outDir: distDir,
      assetsDir: '.',
      sourcemap: false,
      minify: 'esbuild',
      rollupOptions: {
        input: entryPath,
        output: {
          format: 'iife',
          dir: distDir,
          entryFileNames: outFile,
          inlineDynamicImports: true,
          extend: false,
          // Use a unique IIFE name per module to avoid conflicts
          name: name === 'core' ? 'tsjs' : `tsjs_${name}`,
        },
      },
    },
    logLevel: 'warn',
  });

  console.log(`[build-all] Built ${outFile}`);
}

// Build core first (synchronously), then all integrations in parallel
await buildModule('core', path.join(srcDir, 'core', 'index.ts'));

await Promise.all(
  integrationModules.map((name) =>
    buildModule(name, path.join(integrationsDir, name, 'index.ts')),
  ),
);

// List all built files
const builtFiles = fs
  .readdirSync(distDir)
  .filter((f) => f.startsWith('tsjs-') && f.endsWith('.js'))
  .sort();

console.log('[build-all] Built files:', builtFiles);
console.log(`[build-all] Total: ${builtFiles.length} modules`);
