/**
 * Build a publisher-specific external Prebid bundle.
 *
 * Unlike build-all.mjs, this script is intended to run outside the Cargo build.
 * It produces an immutable bundle and manifest that can be hosted on an asset
 * CDN, then referenced by integrations.prebid.external_bundle_url.
 */

import crypto from 'node:crypto';
import fs from 'node:fs';
import { createRequire } from 'node:module';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const require = createRequire(import.meta.url);
const srcDir = path.resolve(__dirname, 'src');
const integrationsDir = path.join(srcDir, 'integrations');
const prebidDir = path.join(integrationsDir, 'prebid');

const DEFAULT_PREBID_ADAPTERS = 'rubicon';
const ADAPTERS_GENERATED_SPECIFIER = './_adapters.generated';
const USER_IDS_GENERATED_SPECIFIER = './_user_ids.generated';
const USER_ID_REGISTRY_FILE = path.join(prebidDir, 'user_id_modules.json');
const LIVE_INTENT_SHIM_ALIAS = 'prebid.js/modules/liveIntentIdSystem.js';
const PREBID_PACKAGE_DIR = path.join(__dirname, 'node_modules', 'prebid.js');
const PREBID_LIVE_INTENT_STANDARD = path.join(
  PREBID_PACKAGE_DIR,
  'dist',
  'src',
  'libraries',
  'liveIntentId',
  'idSystem.js'
);
const PREBID_GLOBAL_MODULE = path.join(PREBID_PACKAGE_DIR, 'dist', 'src', 'src', 'prebidGlobal.js');
const LIVE_INTENT_SHIM = path.join(prebidDir, 'prebid_modules', 'liveIntentIdSystem.ts');

export function parseArgs(argv) {
  const options = new Map();
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (!arg.startsWith('--')) {
      throw new Error(`[build-prebid-external] Unexpected positional argument: ${arg}`);
    }

    const equalsIndex = arg.indexOf('=');
    const rawKey = equalsIndex === -1 ? arg.slice(2) : arg.slice(2, equalsIndex);
    const inlineValue = equalsIndex === -1 ? undefined : arg.slice(equalsIndex + 1);
    const value = inlineValue ?? argv[i + 1];
    if (!value || value.startsWith('--')) {
      throw new Error(`[build-prebid-external] Missing value for --${rawKey}`);
    }
    if (inlineValue === undefined) {
      i += 1;
    }
    options.set(rawKey, value);
  }

  return {
    adapters: parseList(options.get('adapters') ?? DEFAULT_PREBID_ADAPTERS),
    userIdModules: options.has('user-id-modules')
      ? parseList(options.get('user-id-modules'))
      : null,
    outDir: path.resolve(process.cwd(), options.get('out') ?? path.join('..', 'dist', 'prebid')),
  };
}

function parseList(raw) {
  return raw
    .split(',')
    .map((value) => value.trim())
    .filter(Boolean);
}

function requireExistingFile(filePath, description) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`[build-prebid-external] Missing ${description}: ${filePath}`);
  }
}

function prebidPackageVersion() {
  const packageJsonPath = path.join(PREBID_PACKAGE_DIR, 'package.json');
  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
  return packageJson.version;
}

function readUserIdRegistry() {
  return JSON.parse(fs.readFileSync(USER_ID_REGISTRY_FILE, 'utf8'));
}

function validateUserIdImport(entry) {
  requireExistingFile(LIVE_INTENT_SHIM, 'LiveIntent ESM shim');
  requireExistingFile(PREBID_LIVE_INTENT_STANDARD, 'Prebid LiveIntent standard ESM module');
  requireExistingFile(PREBID_GLOBAL_MODULE, 'Prebid global module');

  if (entry.moduleName === 'liveIntentIdSystem') {
    return;
  }

  try {
    require.resolve(entry.importPath, { paths: [__dirname] });
  } catch (error) {
    throw new Error(
      `[build-prebid-external] Required Prebid user ID module "${entry.moduleName}" ` +
        `could not be resolved from ${entry.importPath}: ${error.message}`
    );
  }
}

function writeGeneratedModule(filePath, title, moduleNames, imports, exports = []) {
  const content = [
    '// Auto-generated by build-prebid-external.mjs.',
    '//',
    title,
    `// Modules: ${moduleNames.join(', ')}`,
    '',
    ...imports,
    ...(exports.length > 0 ? ['', ...exports] : []),
    '',
  ].join('\n');

  fs.writeFileSync(filePath, content);
}

export function renderIncludedUserIdModulesExport(moduleNames) {
  return `export const INCLUDED_PREBID_USER_ID_MODULES = ${JSON.stringify(moduleNames)};`;
}

function generateAdapterImports(adapterNames, adaptersFile) {
  const modulesDir = path.join(PREBID_PACKAGE_DIR, 'modules');
  const imports = [];
  const validAdapters = [];

  for (const name of adapterNames) {
    const moduleFile = `${name}BidAdapter.js`;
    const modulePath = path.join(modulesDir, moduleFile);
    if (!fs.existsSync(modulePath)) {
      throw new Error(
        `[build-prebid-external] Prebid adapter "${name}" not found (expected ${moduleFile})`
      );
    }
    imports.push(`import 'prebid.js/modules/${moduleFile}';`);
    validAdapters.push(name);
  }

  writeGeneratedModule(
    adaptersFile,
    '// External Prebid bundle adapter imports.',
    validAdapters,
    imports
  );
  return validAdapters;
}

function generateUserIdImports(requestedModules, userIdsFile) {
  const registry = readUserIdRegistry();
  const entriesByModule = new Map(registry.modules.map((entry) => [entry.moduleName, entry]));
  const moduleNames = requestedModules ?? registry.defaultPreset;
  const selectedEntries = moduleNames.map((moduleName) => {
    const entry = entriesByModule.get(moduleName);
    if (!entry) {
      throw new Error(`[build-prebid-external] Unknown Prebid user ID module: ${moduleName}`);
    }
    validateUserIdImport(entry);
    return entry;
  });

  const imports = selectedEntries.map((entry) => `import '${entry.importPath}';`);
  writeGeneratedModule(
    userIdsFile,
    '// External Prebid bundle User ID module imports.',
    moduleNames,
    imports,
    [renderIncludedUserIdModulesExport(moduleNames)]
  );
  return moduleNames;
}

function createTemporaryModulePaths() {
  const temporaryDir = fs.mkdtempSync(path.join(prebidDir, '.external-generated-'));
  return {
    temporaryDir,
    adaptersFile: path.join(temporaryDir, '_adapters.generated.ts'),
    userIdsFile: path.join(temporaryDir, '_user_ids.generated.ts'),
    entryFile: path.join(temporaryDir, '_external_entry.generated.ts'),
  };
}

function generateExternalEntry(entryFile, adapters) {
  const content = [
    '// Auto-generated by build-prebid-external.mjs.',
    '//',
    '// Pure Prebid.js external bundle: core, consent modules, user ID modules,',
    '// and client-side bid adapters. The Trusted Server prebid shim',
    '// (tsjs-prebid, served by the server) installs the trustedServer adapter',
    '// onto the `window.pbjs` global this bundle populates and drives queue',
    '// processing — this bundle intentionally does NOT call processQueue().',
    "import 'prebid.js';",
    "import 'prebid.js/modules/consentManagementTcf.js';",
    "import 'prebid.js/modules/consentManagementGpp.js';",
    "import 'prebid.js/modules/consentManagementUsp.js';",
    "import 'prebid.js/modules/userId.js';",
    "import './_adapters.generated';",
    "import { INCLUDED_PREBID_USER_ID_MODULES } from './_user_ids.generated';",
    '',
    '// Manifest consumed by the tsjs prebid shim to validate that every',
    '// configured client_side_bidder has its adapter compiled in.',
    '(window as unknown as Record<string, unknown>).__tsjs_prebid_bundle = Object.freeze({',
    `  adapters: ${JSON.stringify(adapters)},`,
    '  userIdModules: INCLUDED_PREBID_USER_ID_MODULES,',
    '});',
    '',
  ].join('\n');

  fs.writeFileSync(entryFile, content);
}

export function deriveBundleMetadata(bundleBytes) {
  const sha256 = crypto.createHash('sha256').update(bundleBytes).digest('hex');
  const sri = `sha384-${crypto.createHash('sha384').update(bundleBytes).digest('base64')}`;
  const filename = `trusted-prebid-${sha256}.js`;

  return { filename, sha256, sri };
}

async function buildExternalBundle(outDir, generatedModules) {
  fs.mkdirSync(outDir, { recursive: true });

  const temporaryFile = `trusted-prebid-${process.pid}-${crypto.randomUUID()}.tmp.js`;
  const temporaryPath = path.join(outDir, temporaryFile);
  let renamedTemporaryFile = false;

  try {
    const { build } = await import('vite');

    await build({
      configFile: false,
      root: __dirname,
      resolve: {
        alias: [
          { find: ADAPTERS_GENERATED_SPECIFIER, replacement: generatedModules.adaptersFile },
          { find: USER_IDS_GENERATED_SPECIFIER, replacement: generatedModules.userIdsFile },
          { find: LIVE_INTENT_SHIM_ALIAS, replacement: LIVE_INTENT_SHIM },
          { find: 'prebid.js/modules/liveIntentIdSystem', replacement: LIVE_INTENT_SHIM },
          {
            find: 'tsjs-prebid/liveIntentIdSystemStandard',
            replacement: PREBID_LIVE_INTENT_STANDARD,
          },
          { find: 'tsjs-prebid/prebidGlobal', replacement: PREBID_GLOBAL_MODULE },
          {
            find: 'prebid.js/src/adapterManager.js',
            replacement: path.resolve(
              __dirname,
              'node_modules/prebid.js/dist/src/src/adapterManager.js'
            ),
          },
          {
            find: 'prebid.js/src/adRendering.js',
            replacement: path.resolve(
              __dirname,
              'node_modules/prebid.js/dist/src/src/adRendering.js'
            ),
          },
        ],
      },
      build: {
        emptyOutDir: false,
        outDir,
        assetsDir: '.',
        sourcemap: false,
        minify: 'esbuild',
        rollupOptions: {
          input: generatedModules.entryFile,
          output: {
            format: 'iife',
            dir: outDir,
            entryFileNames: temporaryFile,
            inlineDynamicImports: true,
            extend: false,
            name: 'tsjs_prebid_external',
          },
        },
      },
      logLevel: 'warn',
    });

    const bundleBytes = fs.readFileSync(temporaryPath);
    const metadata = deriveBundleMetadata(bundleBytes);
    const finalPath = path.join(outDir, metadata.filename);

    fs.rmSync(finalPath, { force: true });
    fs.renameSync(temporaryPath, finalPath);
    renamedTemporaryFile = true;

    return metadata;
  } finally {
    if (!renamedTemporaryFile) {
      fs.rmSync(temporaryPath, { force: true });
    }
  }
}

export async function main(argv = process.argv.slice(2)) {
  const args = parseArgs(argv);
  const generatedModules = createTemporaryModulePaths();

  try {
    const adapters = generateAdapterImports(args.adapters, generatedModules.adaptersFile);
    const userIdModules = generateUserIdImports(args.userIdModules, generatedModules.userIdsFile);
    generateExternalEntry(generatedModules.entryFile, adapters);
    const bundle = await buildExternalBundle(args.outDir, generatedModules);
    const manifest = {
      prebidVersion: prebidPackageVersion(),
      adapters,
      userIdModules,
      sha256: bundle.sha256,
      sri: bundle.sri,
      filename: bundle.filename,
    };

    fs.writeFileSync(
      path.join(args.outDir, 'manifest.json'),
      `${JSON.stringify(manifest, null, 2)}\n`
    );

    console.log('[build-prebid-external] Built external Prebid bundle:', bundle.filename);
    console.log('[build-prebid-external] SHA-256:', bundle.sha256);
    console.log('[build-prebid-external] SRI:', bundle.sri);
    console.log('[build-prebid-external] Manifest:', path.join(args.outDir, 'manifest.json'));
  } finally {
    fs.rmSync(generatedModules.temporaryDir, { recursive: true, force: true });
  }
}

if (process.argv[1] && import.meta.url === pathToFileURL(path.resolve(process.argv[1])).href) {
  await main();
}
